-- CRIAÇÃO DE TABELAS
CREATE TABLE ALUNO (
    RA NUMBER,
    NOME VARCHAR(40),
    IDADE NUMBER,
    TURMA CHAR(1)
);

-- ADIÇÃO DE CONSTRAINTS
ALTER TABLE ALUNO ADD CONSTRAINT RA_UNIQUE UNIQUE (RA);


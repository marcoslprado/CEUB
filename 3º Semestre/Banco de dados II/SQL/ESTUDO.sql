-- ADIÇÃO DE COLUNA NA TABELA
ALTER TABLE ALUNO ADD FEIO CHAR(1);

-- FUNÇÃO QUE CONSULTA O BANCO DE DADOS E O ORDENA CRESCENTEMENTE POR UMA COLUNA ESPECÍFICA
SELECT * FROM ALUNO ORDER BY RA;

-- MESMA COISA MAIS ORDENA DECRESCENTE
SELECT * FROM ALUNO ORDER BY RA DESC;

-- CONTA O NUMERO DE ALUNOS CADASTRADOS
SELECT COUNT(NOME) FROM ALUNO;

-- CONTA QUANTAS IDADES DIFERENTES ESTÃO CADASTRADAS NO B.D.
SELECT COUNT(DISTINCT IDADE) FROM ALUNO;

-- TRAZ A SOMA DAS IDADES DE TODOS OS ALUNOS CADASTRADOS
SELECT SUM(IDADE) FROM ALUNO;

-- TRAZ A MEDIA DAS IDADES DE TODOS OS ALUNOS CADASTRADOS
SELECT AVG(IDADE) FROM ALUNO;

-- TRAZ A IDADE MINIMA CADASTRADA
SELECT MIN(IDADE) FROM ALUNO;

-- TRAZ O MAIOR RA CADASTRADO FAZENDO O NOME DA TABELA VIRAR MATRICULA NO RESULTADO
SELECT MAX(RA) AS MATRICULA FROM ALUNO;